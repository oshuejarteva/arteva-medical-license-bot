package com.arteva.medbot.model;

import java.util.List;

/**
 * Ответ RAG-пайплайна на вопрос пользователя.
 * <p>
 * Содержит сгенерированный LLM ответ и список источников (имена файлов),
 * из которых была извлечена информация.
 *
 * @param answer  текст ответа, сгенерированный на основе найденных фрагментов документов.
 *                Если релевантных фрагментов не найдено — содержит стандартное сообщение.
 * @param sources список имён файлов-источников (например, {@code ["license.docx", "order.doc"]}).
 *                Пустой список, если информация не найдена.
 */
public record AskResponse(
        String answer,
        List<String> sources
) {

    /**
     * Фабричный метод для случая, когда в документах нет релевантной информации.
     *
     * @return ответ со стандартным сообщением и пустым списком источников
     */
    public static AskResponse noInfo() {
        return new AskResponse(
                "В документах нет информации по данному вопросу.",
                List.of()
        );
    }
}
